package com.somin.枚举;

public interface SeasonInterface {
	enum spring implements SeasonInterface{
		spring_SWIMMINGH,spring_READING,spring_LEARNING,spring_RUNNING
	}
	enum summer implements SeasonInterface{
		summer_SWIMMINGH,summer_READING,summer_LEARNING,summer_RUNNING
	}
	enum autumn implements SeasonInterface{
		autumn_SWIMMINGH,autumn_READING,autumn_LEARNING,autumn_RUNNING
	}
	enum winter implements  SeasonInterface{
		winter_SWIMMINGH,winter_READING,winter_LEARNING,winter_RUNNING
	}
}
